def tr(key: str, **kwargs) -> str:
    from app.config import get_config
    cfg = get_config()
    lang = cfg.get("ui_lang", "ru")
    d = TRANSLATIONS.get(lang, TRANSLATIONS["ru"])
    text = d.get(key, TRANSLATIONS["ru"].get(key, key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return text
